//WEEK ONE EXERCISES
//EX. 04 Print the average of three numbers

public class ex04Average3Numbers
{
    public static void main(String[] args)
    {
        System.out.println("Displaying the average of three numbers.");
        int myfirstNumber = 23;
        int mySecondNumber = 11;
        int myThirdNumber = 77;
        int myAverage = (myfirstNumber + mySecondNumber + myThirdNumber)/3;

        System.out.println("Average: (" + myfirstNumber + " + " + mySecondNumber + " + " + myThirdNumber + ") /3 = " + myAverage);
    }
}
